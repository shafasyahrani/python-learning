# OPERASI KOMPARASI

print("===KOMPARASI===\n")

# == != > < >= <=
# hasil komparasi adalah boolean

a = 5
b = 7

#Equal (==)
print("==Equal (==)==")
hasil = a == b
print("hasil (a == b):", hasil)
hasil = a == 5
print("hasil (a == 5):", hasil)

#Not Equal (!=)
print("===NOT Equal (!=)===")
hasil = a != b
print("hasil (a != b):", hasil)
hasil = b != 7
print("hasil (b != 7):", hasil)

#Lebih besar dari >
print("===Greater than (>)===")
hasil = a > b
print("hasil (a > b):",hasil)
hasil = a > 2
print("hasil (a > 2)", hasil)

#Lebih kecil dari (<)
print("===Lees than (<)===")
hasil = a < b
print("hasil (a < b):", hasil)
hasil = b < 8
print("hasil (b < 8):", hasil)

#Lebih besar atau sama dengan
print("==Greater than or Equal to (>=)==")
hasil = a >= b
print("hasil (a >= b):",hasil)
hasil = a >= 5
print("hasil (a >= b):", hasil)

#Lebih kecil atau sama dengan
print("==Less than or Equal to (<=)==")
hasil = a <= b
print("hasil (a <= b):", hasil)
hasil = a <= 5
print("hasil (a <= 5):", hasil)