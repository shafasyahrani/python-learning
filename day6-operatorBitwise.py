#OPERATOR BITWISE

#digunakan untuk membandingkan nomor
#AND OR XOR NOT Shiftleft ShiftRight

a = 5
b = 3

#AND (&)
c = a & b
print("nilai c adalah:",c)

#OR (|)
c = a | b
print("nilai c adalah:",c)

#XOR (^)
c = a ^ b
print("nilai c adalah:",c)

#NOT (~)
c = ~a
print("nilai c adalah:",c)

#Shif Left (<<)
c = a << b
print("nilai c adalah:",c)

#Shift Right (>>)
c = a >> b
print("nilai c adalah:", c)
