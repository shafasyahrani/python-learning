#TIPE DATA DI PYTHON

#Integer (int) : angka satuan yg bukan koma
a = 6
print((a), " ini adalah angka integer")
print("tipe angka ini adalah ", type(a))

#Float : angka koma
b = 1.5
print((b), " ini adalah angka float")
print("tipe angka ini adalah ", type(b))

#String : kumpulan karakter
c = "Nama"
print((c), " ini adalah angka String")
print("tipe ini adalah ", type(c))

#boolean : logika
d = True
print((d), " ini adalah angka boolean")
print("tipe ini adalah ", type(d))

#complex
data_kompleks = complex(1,2)
print((data_kompleks), " ini adalah angka complex")
print("tipe angka ini adalah ", type(data_kompleks))

